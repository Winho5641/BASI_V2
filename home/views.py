        Analy = list(set([tuple(Arr) for Arr in AnalyData]))

        ## 중복 제거로 인한 순서가 랜덤으로 바뀌어서 정렬
        Analy.sort(key=lambda x: (x[0], x[1]))

        ## 정렬된 데이터 Reverse
        Analy.reverse()

        ## 데이터를 DataFrame으로 전환
        Analy = pd.DataFrame(Analy, columns=["day", "time", "title", "content", "good", "bad"])

        context['Analys'] = Analy

        ## 크롤링 데이터가 비었을 경우
        if(Analy.empty) :
            context['Count_graph'] = "데이터가 존재하지 않습니다."
            context['Word_cloud'] = "데이터가 존재하지 않습니다."
            context['Sent_graph'] = "데이터가 존재하지 않습니다."
            return context


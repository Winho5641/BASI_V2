        stock_noun_list = crawling.Noun_filter(stock_title_pos, stock_content_pos)

        ## 추출한 데이터 2글자 이상만 필터링
        stock_noun_list = [n for n in stock_noun_list if len(n) > 1]

        ## 단어별 개수세기
        counts = Counter(stock_noun_list)
        tags = counts.most_common(50)  ## 가장 많은 50개의 명사 추출


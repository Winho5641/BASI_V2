        pos_post = neg_post = 0  ## 긍정 게시물과 부정 게시물 Count
        for score in sent_score:
            if (score > 0):
                pos_post += 1
            elif (score < 0):
                neg_post += 1

        ## Sentiment Pie Graph 함수
        context['Sent_graph'] = crawling.Sentiment_graph(pos_post, neg_post)


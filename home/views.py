        context['Count_graph'] = crawling.Count_Graph(Analy.day)

        ## title, content 불용어제거 + 형태소 분석 실행
        stock_title_pos = crawling.title_pos(Analy)
        stock_content_pos = crawling.content_pos(Analy)

